package com.Sbarrera.ejercicio1;


public class OperacionesLogicas {

    public static int suma(int valor_1, int valor_2){
        int suma;
        suma = valor_1 + valor_2;
        return suma;
    }

    public static double resta(double valor_1, double valor_2){
        double resta;
        resta = valor_1 - valor_2;
        return resta;
    }

    public static int multiplicacion(int valor_1, int valor_2){
        int multiplicacion;
        multiplicacion = valor_1 * valor_2;
        return multiplicacion;
    }

    public static float division(float valor_1, float valor_2){
        float division;
        division = valor_1 / valor_2;
        return division;
    }

    public static double residuo(int valor_1, int valor_2){
        double division;
        division = valor_1 % valor_2;
        return division;
    }

}
